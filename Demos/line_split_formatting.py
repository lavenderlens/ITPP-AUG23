technology_without_an_interesting_name = "TWAIN"  # double OR single quotes

# split on \
if technology_without_an_interesting_name == \
        technology_without_an_interesting_name:
    print("The TWAIN scanner interface")

# group by ()
if (technology_without_an_interesting_name ==
        technology_without_an_interesting_name):
    print("The TWAIN scanner interface")

# group by {}
if {technology_without_an_interesting_name ==
        technology_without_an_interesting_name}:
    print("The TWAIN scanner interface")
